/* Set a single pixel in the framebuffer */
static inline void fb_set_pixel(int x, int y, uint32_t color)
{
    if (x < 0 || x >= FB_WIDTH || y < 0 || y >= FB_HEIGHT)
        return; /* Ignore out-of-bounds pixels */
    volatile uint32_t *fb = (uint32_t *)FB_BASE;
    fb[y * FB_WIDTH + x] = color;
}

/* Draw a character from the font array at (x, y) */
static inline void fb_draw_char(int x, int y, char c, uint32_t color)
{
    for (int row = 0; row < CHAR_HEIGHT; row++) {
        unsigned char row_data = letters[(unsigned char)c][row];
        for (int col = 0; col < CHAR_WIDTH; col++) {
            if (row_data & (1 << (7 - col))) { /* Check if the bit is set */
                fb_set_pixel(x + col, y + row, color);
            }
        }
    }
}

/* Print a single character to the framebuffer */
static inline void PUTC_LL(char c)
{
    static int cursor_x = 0, cursor_y = 0; /* Cursor position */
    const uint32_t white = 0xFFFFFFFF;    /* White color in ARGB8888 */

    /* Handle newline */
    if (c == '\n') {
        cursor_x = 0;
        cursor_y += CHAR_HEIGHT;
        if (cursor_y >= FB_HEIGHT) /* Scroll or wrap */
            cursor_y = 0;
        return;
    }

    /* Draw the character and update cursor position */
    fb_draw_char(cursor_x, cursor_y, c, white);
    cursor_x += CHAR_WIDTH;
    if (cursor_x >= FB_WIDTH) { /* Move to the next line if out of width */
        cursor_x = 0;
        cursor_y += CHAR_HEIGHT;
        if (cursor_y >= FB_HEIGHT)
            cursor_y = 0;
    }
}

